// Импортируем нужные "инструменты" из React
// useState - для хранения данных, которые могут меняться
// useEffect - для выполнения кода в определённые моменты (например, при загрузке)
import { useState, useEffect } from 'react'

// Импортируем стили для этого компонента
import './App.css'

// Создаём компонент App. Компонент - это функция, которая возвращает интерфейс
function App() {
  
  // ===== СОСТОЯНИЯ (STATE) =====
  // Состояние - это данные, которые React отслеживает и при изменении которых
  // автоматически перерисовывает интерфейс
  
  // Первое состояние: массив заметок
  // notes - текущее значение (пустой массив в начале)
  // setNotes - функция для изменения этого значения
  const [notes, setNotes] = useState([])
  
  // Второе состояние: текст из поля ввода
  // inputValue - текущий текст
  // setInputValue - функция для изменения текста
  const [inputValue, setInputValue] = useState('')
  
  // ===== ЭФФЕКТЫ (USEFFECT) =====
  // useEffect позволяет выполнить код в определённый момент жизни компонента
  
  // ЭФФЕКТ 1: ЗАГРУЗКА ЗАМЕТОК ПРИ ЗАПУСКЕ
  // Пустой массив зависимостей [] означает: выполнить только один раз при первом рендере
  useEffect(() => {
    // Пытаемся получить сохранённые заметки из localStorage браузера
    const savedNotes = localStorage.getItem('notes')
    
    // Если заметки есть (не null и не undefined)
    if (savedNotes) {
      // Превращаем строку JSON обратно в массив и сохраняем в состояние notes
      // JSON.parse - превращает строку в массив/объект
      setNotes(JSON.parse(savedNotes))
    }
  }, [])  // Пустой массив = выполнить один раз при загрузке компонента
  
  // ЭФФЕКТ 2: СОХРАНЕНИЕ ЗАМЕТОК ПРИ ИХ ИЗМЕНЕНИИ
  // Массив зависимостей [notes] означает: выполнять каждый раз, когда notes меняется
  useEffect(() => {
    // Сохраняем массив заметок в localStorage браузера
    // JSON.stringify - превращает массив в строку формата JSON
    localStorage.setItem('notes', JSON.stringify(notes))
  }, [notes])  // Зависимость от notes - запускается при каждом изменении notes
  
  // ===== ФУНКЦИИ-ОБРАБОТЧИКИ =====
  
  // ФУНКЦИЯ 1: ДОБАВЛЕНИЕ ЗАМЕТКИ
  const addNote = () => {
    // Получаем текст из поля ввода и убираем лишние пробелы по краям
    const text = inputValue.trim()
    
    // Если текст пустой (ничего не ввели или одни пробелы)
    if (text === '') {
      return  // Выходим из функции, ничего не делаем
    }
    
    // Добавляем новую заметку в массив
    // ...notes - это оператор spread, он "разворачивает" старый массив
    // [ ...notes, text ] - создаёт новый массив, где все старые заметки + новая в конце
    setNotes([...notes, text])
    
    // Очищаем поле ввода
    setInputValue('')
  }
  
  // ФУНКЦИЯ 2: УДАЛЕНИЕ ЗАМЕТКИ
  // indexToDelete - индекс (номер) заметки, которую нужно удалить
  const deleteNote = (indexToDelete) => {
    // filter создаёт новый массив, оставляя только те элементы, которые прошли проверку
    // (_, index) => index !== indexToDelete
    // _ - значение заметки (не используем, поэтому _)
    // index - индекс текущего элемента
    // оставляем только те элементы, у которых индекс НЕ равен удаляемому
    const updatedNotes = notes.filter((_, index) => index !== indexToDelete)
    
    // Сохраняем отфильтрованный массив (без удалённой заметки)
    setNotes(updatedNotes)
  }
  
  // ФУНКЦИЯ 3: ОБРАБОТЧИК НАЖАТИЯ КЛАВИШ
  const handleKeyPress = (event) => {
    // Если нажата клавиша Enter И одновременно зажат Ctrl
    if (event.key === 'Enter' && event.ctrlKey) {
      addNote()  // Добавляем заметку
    }
  }
  
  // ===== ВОЗВРАЩАЕМ ИНТЕРФЕЙС (JSX) =====
  // JSX - это HTML-подобный синтаксис внутри JavaScript
  // Всё, что возвращает функция - отображается на экране
  
  return (
    <>
      {/* Это фрагмент (<>...</>). Позволяет возвращать несколько элементов без лишней обёртки */}
      
      {/* ФОНОВЫЕ АНИМИРОВАННЫЕ КРУГИ */}
      <div className="background">
        <span></span>
        <span></span>
        <span></span>
        <span></span>
      </div>

      {/* ОСНОВНОЙ КОНТЕЙНЕР С ЗАМЕТКАМИ */}
      <div className="container">
        <h1>📝 Мои заметки</h1>

        {/* ПОЛЕ ДЛЯ ВВОДА */}
        {/* value={inputValue} - привязываем значение к состоянию */}
        {/* onChange - срабатывает при каждом изменении текста */}
        {/* onKeyDown - срабатывает при нажатии клавиши */}
        <textarea
          id="noteInput"
          placeholder="Напиши что-нибудь... (Ctrl+Enter для добавления)"
          value={inputValue}
          onChange={(e) => setInputValue(e.target.value)}
          onKeyDown={handleKeyPress}
        />

        {/* КНОПКА ДОБАВЛЕНИЯ */}
        {/* onClick - срабатывает при клике на кнопку */}
        <button onClick={addNote}>
          Добавить
        </button>

        {/* КОНТЕЙНЕР ДЛЯ СПИСКА ЗАМЕТОК */}
        <div id="notes">
          {/* Условный рендеринг: если заметок нет, показываем сообщение */}
          {notes.length === 0 ? (
            // Если заметок нет (true)
            <div style={{ textAlign: 'center', opacity: 0.7, marginTop: 20 }}>
              ✨ Нет заметок. Добавьте первую!
            </div>
          ) : (
            // Если заметки есть (false) - перебираем массив и рендерим каждую
            // map - превращает массив в массив JSX элементов
            notes.map((note, index) => (
              // key - уникальный идентификатор (React требует для списков)
              <div className="note" key={index}>
                {/* Текст заметки */}
                {note}
                {/* Крестик удаления */}
                <span 
                  className="delete" 
                  onClick={() => deleteNote(index)}  // При клике удаляем заметку с этим индексом
                >
                  ✖
                </span>
              </div>
            ))
          )}
        </div>
      </div>
    </>
  )
}

// Экспортируем компонент, чтобы он был доступен в других файлах
export default App